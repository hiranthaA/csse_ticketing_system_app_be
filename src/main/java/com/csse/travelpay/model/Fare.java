package com.csse.travelpay.model;

public class Fare {
    private int fareIndex;
    private double amount;

    public int getFareIndex() {
        return fareIndex;
    }

    public void setFareIndex(int fareIndex) {
        this.fareIndex = fareIndex;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }
}
